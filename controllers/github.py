@app.route('/salvar/', methods=['GET','POST'])
def salvar():
  if(request.method == 'POST'):
    git_dict = ast.literal_eval(request.form['github'])
    followers_dict = ast.literal_eval(request.form['followers'])
    following_dict = ast.literal_eval(request.form['following'])
    gituser = Gituser(id=git_dict['id'], login=git_dict['login'], name=git_dict['name'], bio=git_dict['bio'])
    try:
      db.session.add(gituser)
      db.session.commit()
    except:
      db.session.rollback()
    return redirect(url_for('.index'))
  else:
    return 'Não autorizado', 403

@app.route('/registros/', methods=['GET','POST'])
def registros():
  if(request.method == 'GET'):
    gituser = Gituser.query.all()
    result = [user.to_dict() for user in gituser]
    return render_template('registros.html', gitusers=result)
  else:
    gituser = Gituser.query.get(request.form['id'])
    try:
      db.session.delete(gituser)
      db.session.commit()
    except:
      db.session.rollback()
    gituser = Gituser.query.all()
    result = [user.to_dict() for user in gituser]
    return render_template('registros.html', gitusers=result)